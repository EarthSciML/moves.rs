//! Full-suite regression pass — Phase 7 Task 126 (`mo-uj3ke`), expanded by
//! Task 148 (`mo-6oj`).
//!
//! Runs **all 40 characterization fixtures** (30 onroad + 10 NONROAD; the 3
//! `scale-*` fixtures that require additional input databases and the 3
//! `error-*` fixtures that are expected to fail are both excluded from the
//! smoke-test suite) through the complete Rust port and verifies:
//!
//! # Scale fixtures (County / Project / Rates)
//!
//! The three `scale-*` fixtures (`scale-county`, `scale-project`,
//! `scale-rates`) require a County/Project-scale input directory (CDB/PDB
//! Parquet) that is not bundled in the repo.  Set
//! [`SCALE_INPUTS_DIR_ENV`] to a directory containing the per-fixture
//! sub-directories (e.g. `<dir>/scale-county/*.parquet`) to activate the
//! scale-fixture gate.  Without the env var these tests are **dormant** —
//! they print a notice and pass.
//!
//! When `SCALE_INPUTS_DIR` is set **and** canonical MOVES snapshots are
//! available under [`SNAPSHOTS_DIR_ENV`], the canonical-diff gate activates
//! for the scale fixtures too, using the same `characterization/tolerance.toml`
//! budget as the default-scale fixtures.
//!
//! # Canonical MOVES availability
//!
//! The canonical MOVES SIF (`characterization/apptainer/moves-fixture.sif`)
//! was **not present** on the implementation machine at Task 144 author time.
//! Reference snapshots for `scale-county`, `scale-project`, and `scale-rates`
//! therefore need to be generated by a separate canonical-MOVES run on an
//! Apptainer-capable HPC node (see `characterization/run-all-fixtures.sh
//! --include scale-county --include scale-project --include scale-rates`).
//! Until those snapshots exist, the canonical-diff gate for the scale
//! fixtures remains dormant even when `SCALE_INPUTS_DIR` is set.
//!
//! 1. **No fixture panics or errors** — every `moves run` invocation returns
//!    `Ok(outcome)` and produces a `MOVESRun.parquet` metadata file.
//! 2. **Module plan is non-empty** — every fixture exercises at least one
//!    calculator-graph module, confirming the RunSpec parses and the DAG
//!    filters correctly for both the onroad and NONROAD model paths.
//! 3. **Canonical-snapshot diff (active)** — [`canonical_snapshot_diff`] runs
//!    each fixture with `--snapshot` (so the calculators execute against the
//!    captured execution DB and the engine writes the real `MOVESOutput/`
//!    tree), then compares the port's per-pollutant `emissionQuant` totals
//!    against the canonical `MOVESOutput` table. It hard-asserts on the
//!    fixtures whose data plane matches canonical within a documented
//!    precision-only tolerance and prints — without asserting — the divergence
//!    for fixtures with a known, reported data-plane bug. See the catalogue in
//!    `docs/known-divergences.md`.
//!
//! # Why per-pollutant sums, not a cell diff
//!
//! Even where the port reproduces canonical to `f64` precision, the two
//! `MOVESOutput` tables disagree on metadata/labeling columns that do not
//! affect emitted mass (`iterationID`, `roadTypeID`, the `SCC` road-type
//! subfield) and on which uncertainty columns are present. A cell-level
//! [`moves_snapshot::diff_snapshots`] therefore fails on every fixture; the
//! per-pollutant `emissionQuant` total is the quantity that must agree and is
//! the same metric `characterization/audit/regression_gate.sh` uses.
//!
//! # Running the gate explicitly
//!
//! ```sh
//! cargo test --test full_suite_regression canonical_snapshot_diff -- --nocapture
//! ```
//!
//! Point it at a different snapshot tree with
//! `REGRESSION_SNAPSHOTS_DIR=<path>`.

