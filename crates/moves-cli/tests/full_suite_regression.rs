/// Compare each fixture's **real `MOVESOutput`** against its canonical-MOVES
/// snapshot, per-pollutant, with `--snapshot` active.
///
/// **Dormant** unless [`SNAPSHOTS_DIR_ENV`] points at a populated snapshot tree
/// (or the in-repo `characterization/snapshots/` tree is populated, which it is)
/// AND the fixture's snapshot sub-directory contains a `manifest.json`.
///
/// For every covered fixture the test:
/// 1. Runs the port with `snapshot: Some(<canonical-dir>)`, so the calculators
///    execute against the captured execution database and the engine writes the
///    real `MOVESOutput/` partitioned Parquet tree (not just `MOVESRun.parquet`).
/// 2. Sums `emissionQuant` per `pollutantID` from both the canonical
///    `MOVESOutput` table and the port's `MOVESOutput/` tree.
/// 3. Compares the per-pollutant totals.
///
/// Why per-pollutant sums rather than a cell-level [`diff_snapshots`]? Even where
/// the port reproduces canonical to `f64` precision, the two `MOVESOutput` tables
/// disagree on metadata/labeling columns that do not affect emitted mass
/// (`iterationID` NULL vs 1, `roadTypeID` 0 vs the link road type, and the `SCC`
/// road-type subfield), and canonical carries `emissionQuantMean`/`Sigma` where
/// the port carries `emissionRate`/`runHash`. A cell diff fails on those for
/// every fixture; the per-pollutant `emissionQuant` total is the quantity that
/// must agree and cleanly isolates real divergences in emitted mass. This is the
/// same metric as `characterization/audit/regression_gate.sh`.
///
/// The gate hard-asserts on [`asserted_fixtures`] and prints — without asserting
/// — the divergence for [`QUARANTINED_FIXTURES`] (known, reported data-plane
/// bugs; see `docs/known-divergences.md` §4.4).
#[test]
fn canonical_snapshot_diff() {
    let root = snapshots_root();
    let fixtures = all_fixtures();

    let covered: Vec<&Path> = fixtures
        .iter()
        .map(|p| p.as_path())
        .filter(|p| canonical_present(&root, fixture_name(p)))
        .collect();

    if covered.is_empty() {
        println!(
            "\n[canonical_snapshot_diff] DORMANT\n\
             No canonical snapshots under '{}'.\n\
             See docs/known-divergences.md for how to enable this gate.",
            root.display()
        );
        return;
    }

    println!(
        "\n[canonical_snapshot_diff] {} fixture(s) against {}",
        covered.len(),
        root.display()
    );
    println!(
        "{:<28} {:>10} {:>10} {:>14} {:>10}",
        "fixture", "canon_rows", "port_rows", "max_rel_diff", "verdict"
    );
    println!("{}", "-".repeat(78));

    let mut failures: Vec<String> = Vec::new();
    let mut passed = 0usize;
    let mut quarantined = 0usize;

    for fixture_path in covered {
        let name = fixture_name(fixture_path);
        let out = tempdir().expect("tempdir");

        let outcome = run_simulation(&RunOptions {
            runspec: fixture_path.to_path_buf(),
            output: out.path().to_path_buf(),
            max_parallel_chunks: 1,
            calculator_dag: None,
            run_date_time: Some("2026-05-21T00:00:00".to_string()),
            // Activate the data plane: calculators execute against the captured
            // execution DB and the engine writes the real MOVESOutput tree.
            snapshot: Some(root.join(name)),
            scale_input: None,
        })
        .unwrap_or_else(|e| panic!("{name}: run error — {e}"));

        let canonical = match Snapshot::load(&root.join(name)) {
            Ok(s) => pollutant_sums_from_snapshot(&s),
            Err(e) => {
                println!("{name:<28} canonical load error: {e}");
                failures.push(format!("{name}: canonical load error: {e}"));
                continue;
            }
        };
        let port = pollutant_sums_from_output_dir(outcome.output_root.as_path())
            .unwrap_or_else(|e| panic!("{name}: reading port MOVESOutput — {e}"));

        let cmp = compare_pollutant_sums(&canonical, &port, REL_DIFF_FLOOR);

        let asserted = asserted_fixtures().iter().find(|(n, _, _)| *n == name);
        let verdict = if let Some((_, tol, vacuous)) = asserted {
            // Hard-asserted fixture.
            let row_mismatch = canonical.row_count != port.row_count;
            let within = cmp.within(*tol);
            if *vacuous {
                if canonical.row_count == 0 && port.row_count == 0 {
                    passed += 1;
                    "PASS(empty)"
                } else {
                    failures.push(format!(
                        "{name}: expected vacuous (0 rows both) but canon={} port={}",
                        canonical.row_count, port.row_count
                    ));
                    "FAIL"
                }
            } else if within && !row_mismatch {
                passed += 1;
                "PASS"
            } else {
                failures.push(format!(
                    "{name}: max_rel_diff={:.3e} (tol {:.0e}), canon_rows={} port_rows={}",
                    cmp.max_rel_diff, tol, canonical.row_count, port.row_count
                ));
                "FAIL"
            }
        } else if is_quarantined(name) {
            quarantined += 1;
            // HARD-FAIL (operator decision): known data-plane bugs turn CI red —
            // it is OK for CI to be red while results are wrong. Each fixture
            // graduates out of QUARANTINED_FIXTURES into asserted_fixtures once
            // its data plane is fixed. See docs/known-divergences.md §4.4.
            failures.push(format!(
                "{name}: KNOWN DATA-PLANE BUG — max_rel_diff={:.3e}, canon_rows={} \
                 port_rows={} — see docs/known-divergences.md §4.4",
                cmp.max_rel_diff, canonical.row_count, port.row_count
            ));
            "BUG(FAIL)"
        } else {
            // A covered fixture that is neither asserted nor quarantined is an
            // unclassified divergence: fail loudly so it gets triaged rather
            // than silently ignored.
            failures.push(format!(
                "{name}: UNCLASSIFIED max_rel_diff={:.3e}, canon_rows={} port_rows={} — \
                 add to asserted_fixtures (with a documented precision-only tolerance) \
                 or QUARANTINED_FIXTURES (with a docs/known-divergences.md bug entry)",
                cmp.max_rel_diff, canonical.row_count, port.row_count
            ));
            "UNCLASS"
        };

        println!(
            "{name:<28} {:>10} {:>10} {:>14.3e} {:>10}",
            canonical.row_count, port.row_count, cmp.max_rel_diff, verdict
        );
    }

    println!("{}", "-".repeat(78));
    println!(
        "{passed} asserted-pass, {quarantined} known-bug hard-fail, \
         {} total failure(s)",
        failures.len()
    );

    assert!(
        failures.is_empty(),
        "canonical-snapshot gate failures (these are regressions in the \
         asserted set or unclassified divergences — fix the data plane, do not \
         widen tolerances to mask a real bug):\n  {}",
        failures.join("\n  ")
    );
}

// ── scale-fixture gate ────────────────────────────────────────────────────────

/// Run the three County/Project/Rates-scale fixtures and check they complete
/// without error.
///
/// **Dormant** unless [`SCALE_INPUTS_DIR_ENV`] points at a populated inputs
/// root (see module docs).  When active, each fixture:
///
/// 1. Runs `moves run` with `scale_input` pointing at its per-fixture
///    sub-directory under the scale-inputs root.
/// 2. Asserts no error, a non-empty module plan, and a `MOVESRun.parquet`
///    metadata file.
///
/// The canonical-diff gate (comparing against a Phase 0 canonical-MOVES
/// snapshot) requires the reference snapshots to be generated separately —
/// see [`SNAPSHOTS_DIR_ENV`] and the module-level docs for details.
#[test]
fn scale_fixtures_run_without_error() {
    let scale_root = match scale_inputs_root() {
        Some(r) => r,
        None => {
            println!(
                "\n[scale_fixtures_run_without_error] DORMANT\n\
                 Set {SCALE_INPUTS_DIR_ENV}=<dir> to a root with scale-county/, \
                 scale-project/, scale-rates/ sub-directories to activate this gate."
            );
            return;
        }
    };

    let scale_fixtures = [
        ("scale-county", "County-scale (CDB) inventory"),
        ("scale-project", "Project-scale (PDB) inventory"),
        ("scale-rates", "Rates-mode (County-scale CDB)"),
    ];

    println!(
        "\n[scale_fixtures_run_without_error] scale inputs root: {}",
        scale_root.display()
    );
    println!(
        "{:<42} {:>8} {:>8} {:>8}",
        "fixture", "planned", "executed", "unimpl"
    );
    println!("{}", "-".repeat(72));

    let mut failures: Vec<String> = Vec::new();

    for (fixture_name, label) in &scale_fixtures {
        let fixture_path = fixtures_dir().join(format!("{fixture_name}.xml"));
        if !fixture_path.is_file() {
            println!("{fixture_name:<42} SKIP (fixture XML not found)");
            continue;
        }

        let scale_input_dir = scale_root.join(fixture_name);
        if !scale_input_dir.is_dir() {
            println!(
                "{fixture_name:<42} SKIP (no scale-input dir: {})",
                scale_input_dir.display()
            );
            continue;
        }

        let out = tempdir().expect("tempdir");
        let result = run_simulation(&RunOptions {
            runspec: fixture_path.clone(),
            output: out.path().to_path_buf(),
            max_parallel_chunks: 1,
            calculator_dag: None,
            run_date_time: Some("2026-05-21T00:00:00".to_string()),
            snapshot: None,
            scale_input: Some(scale_input_dir),
        });

        match result {
            Err(e) => {
                failures.push(format!("{fixture_name}: {e}"));
                println!("{fixture_name:<42} ERROR");
            }
            Ok(outcome) => {
                if !outcome.run_record_path.is_file() {
                    failures.push(format!("{fixture_name}: MOVESRun.parquet missing"));
                }
                if outcome.modules_planned.is_empty() {
                    failures.push(format!("{fixture_name}: no modules planned"));
                }
                println!(
                    "{fixture_name:<42} {:>8} {:>8} {:>8}  ({label})",
                    outcome.modules_planned.len(),
                    outcome.modules_executed.len(),
                    outcome.modules_unimplemented.len(),
                );
            }
        }
    }

    println!("{}", "-".repeat(72));

    assert!(
        failures.is_empty(),
        "scale-fixture regression failures:\n  {}",
        failures.join("\n  ")
    );
}

/// Diff each scale fixture's port output against its canonical-MOVES snapshot.
///
/// **Doubly-dormant**: requires both [`SCALE_INPUTS_DIR_ENV`] (for the
/// CDB/PDB Parquet inputs) and [`SNAPSHOTS_DIR_ENV`] (for the reference
/// canonical-MOVES output).  When one or both are absent the test prints a
/// notice and passes without running any diff.
///
/// Note: at Task 144 author time, canonical MOVES was **not runnable** on the
/// implementation machine (SIF files were absent). The reference snapshots for
/// `scale-county`, `scale-project`, and `scale-rates` must be produced by a
/// separate run of `characterization/run-all-fixtures.sh --include scale-*`
/// on an Apptainer-capable HPC node.
#[test]
fn scale_canonical_snapshot_diff() {
    let scale_root = match scale_inputs_root() {
        Some(r) => r,
        None => {
            println!(
                "\n[scale_canonical_snapshot_diff] DORMANT\n\
                 Set {SCALE_INPUTS_DIR_ENV} to enable scale-fixture canonical diffs."
            );
            return;
        }
    };

    let snaps_root = snapshots_root();
    let scale_fixtures = ["scale-county", "scale-project", "scale-rates"];
    let covered: Vec<&str> = scale_fixtures
        .iter()
        .copied()
        .filter(|name| canonical_present(&snaps_root, name))
        .collect();

    if covered.is_empty() {
        println!(
            "\n[scale_canonical_snapshot_diff] DORMANT\n\
             No canonical scale snapshots under '{}'.\n\
             Generate them with: characterization/run-all-fixtures.sh \
             --include scale-county --include scale-project --include scale-rates\n\
             See docs/known-divergences.md for full instructions.",
            snaps_root.display()
        );
        return;
    }

    let opts = tolerance_opts();
    println!(
        "\n[scale_canonical_snapshot_diff] {} fixture(s) against {}",
        covered.len(),
        snaps_root.display()
    );

    let mut ok: Vec<&str> = Vec::new();
    let mut failures: Vec<String> = Vec::new();

    for fixture_name in &covered {
        let fixture_path = fixtures_dir().join(format!("{fixture_name}.xml"));
        if !fixture_path.is_file() {
            println!("{fixture_name}: SKIP — fixture XML not found");
            continue;
        }

        let scale_input_dir = scale_root.join(fixture_name);
        if !scale_input_dir.is_dir() {
            println!(
                "{fixture_name}: SKIP — scale-input dir {} not found",
                scale_input_dir.display()
            );
            continue;
        }

        let out = tempdir().expect("tempdir");
        let outcome = run_simulation(&RunOptions {
            runspec: fixture_path,
            output: out.path().to_path_buf(),
            max_parallel_chunks: 1,
            calculator_dag: None,
            run_date_time: Some("2026-05-21T00:00:00".to_string()),
            snapshot: None,
            scale_input: Some(scale_input_dir),
        })
        .unwrap_or_else(|e| panic!("{fixture_name}: run error — {e}"));

        let canonical = match Snapshot::load(&snaps_root.join(fixture_name)) {
            Ok(s) => s,
            Err(e) => {
                println!("{fixture_name}: SKIP — canonical load error: {e}");
                continue;
            }
        };

        let port = match Snapshot::load(outcome.output_root.as_path()) {
            Ok(s) => s,
            Err(e) => {
                println!("{fixture_name}: SKIP diff — port output not in snapshot format: {e}");
                continue;
            }
        };

        let diff = diff_snapshots(&canonical, &port, &opts);
        let s = diff.summary();

        if diff.is_empty() {
            ok.push(fixture_name);
        } else {
            failures.push(format!(
                "{fixture_name}: tables_added={} tables_removed={} tables_changed={} \
                 schema_diffs={} rows_added={} rows_removed={} cells_changed={}",
                s.tables_added,
                s.tables_removed,
                s.tables_changed,
                s.schema_diffs,
                s.rows_added,
                s.rows_removed,
                s.cells_changed,
            ));
        }
    }

    if !ok.is_empty() {
        println!("within tolerance ({}):", ok.len());
        for name in &ok {
            println!("  ok {name}");
        }
    }
    if !failures.is_empty() {
        println!("beyond tolerance ({}):", failures.len());
        for f in &failures {
            println!("  FAIL {f}");
        }
    }

    assert!(
        failures.is_empty(),
        "scale canonical-snapshot divergences beyond budget:\n  {}",
        failures.join("\n  ")
    );
}
