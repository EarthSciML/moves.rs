pub const SNAPSHOTS_DIR_ENV: &str = "REGRESSION_SNAPSHOTS_DIR";

/// Environment variable naming the root directory that holds per-fixture
/// County/Project-scale (CDB/PDB) Parquet inputs.
///
/// The layout expected under this root is:
/// ```text
/// <dir>/
/// scale-county/                   ← *.parquet produced by `moves import-cdb`
/// scale-project/                  ← *.parquet produced by the PDB importer
/// scale-rates/                    ← *.parquet (Rates mode uses County-scale CDB)
/// process-crankcase-start-single/ ← Starts.parquet (county starts data)
/// process-extended-idle-single/   ← hotellingHours.parquet + hotellingActivityDistribution.parquet
/// process-apu-single/             ← hotellingHours.parquet + hotellingActivityDistribution.parquet
/// process-crankcase-extidle-single/ ← hotellingHours.parquet + hotellingActivityDistribution.parquet
/// ```
///
/// For the `*-single` fixtures: the in-repo `characterization/county-inputs/` tree
/// is pre-populated with Washtenaw County data (matching the national default so
/// SINGLE-scale output ≈ DEFAULT-scale at hour 7).
///
/// When this variable is unset (or empty) the scale-fixture tests are dormant.
pub const SCALE_INPUTS_DIR_ENV: &str = "SCALE_INPUTS_DIR";

// ── helpers ──────────────────────────────────────────────────────────────────

fn workspace_root() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .and_then(Path::parent)
        .expect("workspace root above crates/moves-cli/")
        .to_path_buf()
}

fn fixtures_dir() -> PathBuf {
    workspace_root().join("characterization/fixtures")
}

/// The canonical snapshot root, overridable by [`SNAPSHOTS_DIR_ENV`].
fn snapshots_root() -> PathBuf {
    std::env::var_os(SNAPSHOTS_DIR_ENV)
        .map(PathBuf::from)
        .filter(|p| !p.as_os_str().is_empty())
        .unwrap_or_else(|| workspace_root().join("characterization").join("snapshots"))
}

/// The scale-inputs root directory, or `None` when [`SCALE_INPUTS_DIR_ENV`] is
/// not set or does not point to a directory.
fn scale_inputs_root() -> Option<PathBuf> {
    std::env::var_os(SCALE_INPUTS_DIR_ENV)
        .map(PathBuf::from)
        .filter(|p| !p.as_os_str().is_empty() && p.is_dir())
}

/// Path to the cell-level tolerance config (used only by the dormant scale-diff gate).
fn tolerance_config_path() -> PathBuf {
    workspace_root().join("characterization/tolerance.toml")
}

/// `DiffOptions` for the dormant scale-fixture cell-level diff gate.
fn tolerance_opts() -> DiffOptions {
    let path = tolerance_config_path();
    if path.is_file() {
        ToleranceConfig::from_file(&path)
            .map(Into::into)
            .unwrap_or_default()
    } else {
        DiffOptions::default()
    }
}

/// All non-`scale-*`, non-`error-*` fixture XML paths in sorted order.
///
/// 46 total fixtures; 3 `scale-*` excluded (require additional input
/// databases), 3 `error-*` excluded (expected parse errors — tested by
/// [`error_fixtures_return_expected_errors`]). Result: 30 onroad (including
/// mixed-onroad-nonroad) + 10 NONROAD = 40 fixtures.
fn all_fixtures() -> Vec<PathBuf> {
    let dir = fixtures_dir();
    let mut paths: Vec<PathBuf> = std::fs::read_dir(&dir)
        .unwrap_or_else(|e| panic!("read fixtures dir {}: {e}", dir.display()))
        .filter_map(|e| e.ok())
        .map(|e| e.path())
        .filter(|p| {
            let name = p.file_name().and_then(|n| n.to_str()).unwrap_or_default();
            p.extension().and_then(|x| x.to_str()) == Some("xml")
                && !name.starts_with("scale-")
                && !name.starts_with("error-")
        })
        .collect();
    paths.sort();
    paths
}

fn fixture_name(path: &Path) -> &str {
    path.file_stem().and_then(|n| n.to_str()).unwrap_or("?")
}

fn canonical_present(snapshots_root: &Path, name: &str) -> bool {
    snapshots_root.join(name).join("manifest.json").is_file()
}

