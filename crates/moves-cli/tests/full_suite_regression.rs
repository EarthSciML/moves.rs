// ── error-fixture gate ────────────────────────────────────────────────────────

/// Every `error-*` fixture must fail to parse and produce a clear, actionable
/// error message (no panics, no cryptic internal errors).
///
/// Each `error-*.xml` in `characterization/fixtures/` is a deliberately
/// invalid RunSpec. The test asserts:
/// 1. `run_simulation` returns `Err(...)` (not `Ok`).
/// 2. The error message contains a human-readable description of the problem
///    — not a Rust panic backtrace or an opaque internal code.
///
/// Added by Task 148 (`mo-6oj`) to characterize the error-handling paths.
#[test]
fn error_fixtures_return_expected_errors() {
    let dir = fixtures_dir();
    let mut error_fixtures: Vec<PathBuf> = std::fs::read_dir(&dir)
        .unwrap_or_else(|e| panic!("read fixtures dir {}: {e}", dir.display()))
        .filter_map(|e| e.ok())
        .map(|e| e.path())
        .filter(|p| {
            let name = p.file_name().and_then(|n| n.to_str()).unwrap_or_default();
            p.extension().and_then(|x| x.to_str()) == Some("xml") && name.starts_with("error-")
        })
        .collect();
    error_fixtures.sort();

    assert!(
        !error_fixtures.is_empty(),
        "no error-* fixtures found under {}",
        dir.display()
    );

    let expected: &[(&str, &str)] = &[
        ("error-bad-geotype", "invalid enum value for geographicselection.type"),
        ("error-bad-model", "invalid enum value for models.model.value"),
        ("error-bad-modelscale", "invalid enum value for modelscale.value"),
    ];

    let mut failures: Vec<String> = Vec::new();

    for fixture in &error_fixtures {
        let name = fixture_name(fixture);
        let out = tempdir().expect("tempdir");
        let result = run_simulation(&RunOptions {
            runspec: fixture.clone(),
            output: out.path().to_path_buf(),
            max_parallel_chunks: 1,
            calculator_dag: None,
            run_date_time: Some("2026-05-21T00:00:00".to_string()),
            snapshot: None,
            scale_input: None,
        });

        match result {
            Ok(_) => {
                failures.push(format!(
                    "{name}: expected Err but got Ok — fixture should trigger a parse error"
                ));
            }
            Err(e) => {
                // Collect the full error chain so we see the root cause, not
                // just the outermost `anyhow` context frame.
                let msg: String = e
                    .chain()
                    .map(|c| c.to_string())
                    .collect::<Vec<_>>()
                    .join(": ");
                // Require a non-panic, human-readable message.
                if msg.contains("panicked at") || msg.contains("thread 'main' panicked") {
                    failures.push(format!("{name}: got a panic instead of a clean error: {msg}"));
                    continue;
                }
                // Check the expected error substring for known fixtures.
                if let Some((_, expected_substr)) =
                    expected.iter().find(|(n, _)| *n == name)
                {
                    if !msg.contains(expected_substr) {
                        failures.push(format!(
                            "{name}: error message does not contain \
                             expected substring {expected_substr:?}; got: {msg}"
                        ));
                    } else {
                        println!("{name}: ok — {msg}");
                    }
                } else {
                    // Unknown error fixture — just require it returns an error.
                    println!("{name}: ok (unknown fixture, got error) — {msg}");
                }
            }
        }
    }

    assert!(
        failures.is_empty(),
        "error-fixture gate failures:\n  {}",
        failures.join("\n  ")
    );
}

// ── canonical-diff gate ───────────────────────────────────────────────────────

/// Per-pollutant relative-difference tolerance for **onroad** fixtures whose
/// data plane is wired and matches canonical. The float-summation order differs
/// between canonical MOVES (MariaDB) and the Rust port (`f64` Polars), producing
/// sub-`1e-4` relative drift on the `emissionQuant` totals. See
/// `docs/known-divergences.md` §4.2.
const ONROAD_REL_TOL: f64 = 1e-3;

/// Per-pollutant relative-difference tolerance for **NONROAD** fixtures. NONROAD
/// arithmetic is Fortran single-precision (`real*4`) in canonical MOVES; the
/// Rust port uses `f64` throughout, so the totals differ by a documented
/// half-percent-scale amount even when the physics is reproduced exactly. See
/// `docs/known-divergences.md` §4.2.
const NONROAD_REL_TOL: f64 = 1e-2;

/// Floor on the relative-difference denominator so a pollutant that is exactly
/// zero in canonical does not yield an infinite relative difference.
const REL_DIFF_FLOOR: f64 = 1e-30;

/// Fixtures whose `MOVESOutput` matches canonical within tolerance and which the
/// gate therefore **hard-asserts**. A regression that pushes any of these out of
/// tolerance fails the test.
///
/// `vacuous` marks fixtures whose canonical snapshot has no `MOVESOutput` rows
/// (the selected process is not materialised in the capture); the port likewise
/// emits nothing, so there is no divergence to measure. They are pinned here so
/// that if either side starts producing rows the asymmetry is caught.
fn asserted_fixtures() -> &'static [(&'static str, f64, bool)] {
    &[
        // (fixture, per-pollutant relative tolerance, vacuous)
        ("process-evap-fvv", ONROAD_REL_TOL, false), // ~8.2e-5
        ("process-evap-leaks", ONROAD_REL_TOL, false), // ~1.6e-7
        ("process-evap-permeation", ONROAD_REL_TOL, false), // ~2.1e-7
        ("nr-commercial-nation", NONROAD_REL_TOL, false), // ~3.5e-3 (real*4)
        // process-apu was asserted-vacuous (canon 0 / port 0) only because the
        // month off-by-one blocked all BaseRate output. With that fixed the
        // BaseRate path now emits the process-91 / opMode-201,203 (APU /
        // shorepower) energy rates, which canonical drops from baseRateOutput
        // (its baserateoutput is 0 even though baserate_91_2020 has 358 rows
        // and a baserateunits row exists). Reproducing that requires the
        // runspec-derived BRC activity gating the port has not yet wired —
        // see QUARANTINED_FIXTURES and docs/known-divergences.md §4.4.
        ("process-crankcase-extidle", ONROAD_REL_TOL, true),
        ("process-crankcase-start", ONROAD_REL_TOL, true),
        ("process-extended-idle", ONROAD_REL_TOL, true),
    ]
}

