/// Number of populated entries in [`EQPCOD`].
///
/// The Fortran source dimensions `eqpcod` to `NEQNAM = 575` but only
/// assigns 562 entries; the rest stay blank-padded. Code that iterates
/// the table should bound by [`EQPCOD_LEN`], not the Fortran ceiling.
pub const EQPCOD_LEN: usize = EQPCOD.len();

/// Maximum dimension declared by the Fortran source (`NEQNAM = 575`
/// in `nonrdusr.inc`). Kept here for cross-reference only.
pub const NEQNAM: usize = 575;

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn table_size_matches_fortran() {
        assert_eq!(EQPCOD_LEN, 562);
    }

    #[test]
    fn first_and_last_codes_match_fortran() {
        assert_eq!(EQPCOD[0], "2260001010");
        assert_eq!(EQPCOD[EQPCOD_LEN - 1], "2285008015");
    }

    #[test]
    fn category_boundary_codes_match_fortran() {
        // Boundary spot-checks lifted directly from iniasc.f comments.
        assert_eq!(EQPCOD[29], "2270001060"); // last Recreational
        assert_eq!(EQPCOD[30], "2260002003"); // first Construction
        assert_eq!(EQPCOD[159], "2270002081"); // last Construction
        assert_eq!(EQPCOD[160], "2260003010"); // first Industrial
        assert_eq!(EQPCOD[194], "2270003070"); // last Industrial
        assert_eq!(EQPCOD[195], "2260004010"); // first Lawn and Garden
        assert_eq!(EQPCOD[329], "2270004076"); // last Lawn and Garden
        assert_eq!(EQPCOD[330], "2260005010"); // first Agricultural
        assert_eq!(EQPCOD[379], "2270005060"); // last Agricultural
        assert_eq!(EQPCOD[380], "2260006005"); // first Commercial
        assert_eq!(EQPCOD[414], "2270006035"); // last Commercial
        assert_eq!(EQPCOD[415], "2260007005"); // first Logging
        assert_eq!(EQPCOD[429], "2270007015"); // last Logging
        assert_eq!(EQPCOD[430], "2260008005"); // first Airport GSE
        assert_eq!(EQPCOD[524], "2270008111"); // last Airport GSE
        assert_eq!(EQPCOD[525], "2260009010"); // first Other Underground Mining
        assert_eq!(EQPCOD[530], "2260010010"); // first Other Oil Field
        assert_eq!(EQPCOD[535], "2280001010"); // first Marine Vessels
        assert_eq!(EQPCOD[551], "2282005010"); // first Pleasure Craft
        assert_eq!(EQPCOD[557], "2285002015"); // first Railroad Locomotives
    }

    #[test]
    fn every_entry_is_ten_chars() {
        for (i, code) in EQPCOD.iter().enumerate() {
            assert_eq!(
                code.len(),
                10,
                "entry {} ({:?}) is not 10 chars wide",
                i + 1,
                code
            );
            assert!(
                code.chars().all(|c| c.is_ascii_digit()),
                "entry {} ({:?}) has non-digit characters",
                i + 1,
                code
            );
        }
    }

    #[test]
    fn neqnam_matches_fortran_dimension() {
        // The Fortran array dimension exceeds the populated count;
        // confirm we preserved that documentation fact.
        assert_eq!(NEQNAM, 575);
        assert_eq!(NEQNAM - EQPCOD_LEN, 13);
    }
}
