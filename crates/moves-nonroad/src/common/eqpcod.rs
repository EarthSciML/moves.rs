//! NONROAD equipment SCC code table (`iniasc.f`).
//!
//! Task 99. The Fortran source initialises a fixed array
//! `eqpcod(NEQNAM)` (declared in `nonrdusr.inc` with `NEQNAM = 575`)
//! with 562 hard-coded SCC codes covering every equipment subcategory
//! the model knows about. The Rust port exposes the same table as a
//! `pub const &[&str]`; the unused tail slots from the Fortran array
//! are simply absent here.
//!
//! The table is consumed by the source-category packet parser (the
//! `lascat` flag array in `intnon.f`'s downstream `rdnrsrc.f` lookup)
//! and by the geography process routines when iterating equipment.
//!
//! # Categories
//!
//! The table is organised by equipment category. Within each
//! category, codes increase in the order assigned in the Fortran
//! source — preserving the original index numbering is essential
//! because several downstream routines treat array indices as
//! equipment-category bins.
//!
//! | Range (1-based) | Category |
//! |---|---|
//! | 1..=30      | Recreational |
//! | 31..=160    | Construction and Mining |
//! | 161..=195   | Industrial |
//! | 196..=330   | Lawn and Garden |
//! | 331..=380   | Agricultural |
//! | 381..=415   | Commercial |
//! | 416..=430   | Logging |
//! | 431..=525   | Airport Ground Support |
//! | 526..=530   | Other Underground Mining |
//! | 531..=535   | Other Oil Field |
//! | 536..=551   | Marine Vessels |
//! | 552..=557   | Pleasure Craft |
//! | 558..=562   | Railroad Locomotives |
//!
//! Total: 562 codes.
//!
//! # Fortran source
//!
//! Ports `iniasc.f` (653 lines).

